class train{
    String name;
    String id;
    String origin;
    String destination;
    String departureTime;

    train(String name, String id, String origin, String destination, String departureTime){
        this.name = name;
        this.id = id;
        this.origin = origin;
        this.destination = destination;
        this.departureTime = departureTime;
    }
}